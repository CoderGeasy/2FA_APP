{
    use Queueable, SerializesModels;

    public $token;

    public function __construct($token)
    {
        $this->token = $token;
    }

    public function build()
    {
        return $this
            ->subject('Your 2FA Verification Code')
            ->view('emails.two-factor-token')
            ->with('token', $this->token);
